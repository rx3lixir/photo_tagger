def get_english_tags(russian_tags):
    """
    Преобразует список русских тэгов в английские (обратная карта)
    
    Args:
        russian_tags: список русских тэгов
        
    Returns:
        список английских тэгов
    """
    reverse_map = {v: k for k, v in TAG_TRANSLATION_MAP.items()}
    return [reverse_map.get(tag, tag) for tag in russian_tags]

